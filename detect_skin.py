        # ‡πÅ‡∏õ‡∏•‡∏ä‡∏∑‡πà‡∏≠ class
        class_names = {
            0: "‡∏õ‡∏Å‡∏ï‡∏¥",
            1: "‡∏°‡∏∞‡πÄ‡∏£‡πá‡∏á‡∏ú‡∏¥‡∏ß‡∏´‡∏ô‡∏±‡∏á", 
            2: "‡πÑ‡∏ù‡∏ú‡∏¥‡∏î‡∏õ‡∏Å‡∏ï‡∏¥",
            3: "‡∏ú‡∏¥‡∏ß‡∏´‡∏ô‡∏±‡∏á‡∏≠‡∏±‡∏Å‡πÄ‡∏™‡∏ö"
            # ‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÉ‡∏´‡πâ‡∏Ñ‡∏£‡∏ö‡∏ï‡∏≤‡∏°‡πÇ‡∏°‡πÄ‡∏î‡∏•‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì
        }

